package ch.heig.gen.labo3;

public class Square {
    private String name;

    /**
     * Constructor of Square.
     * @param name Name of this square.
     */
    public Square(String name) {
        this.name = name;
    }

    /**
     * Get this square's name.
     * @return Instance of String.
     */
    public String getName() {
        return this.name;
    }
}
